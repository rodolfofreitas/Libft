#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*temp;
	size_t	i;

	i = 0;
	if (!s)
		return (NULL);
	if (start > ft_strlen(s))
		return (ft_strdup(""));
	if (len > ft_strlen(&s[start]))
		len = ft_strlen(&s[start]);
	temp = (char *)malloc(len + 1);
	if (!temp)
		return (NULL);
	while (s[start] && i < len)
		temp[i++] = s[start++];
	temp[i] = '\0';
	return (temp);
}
